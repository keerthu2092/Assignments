public class PostiveorNegative {

	public static void main(String[] args) {
		int num=35;
		if(num>0)
		{
			System.out.println(num+ "is positive");
		}
		else if(num<0)
		{
			System.out.println(num+ "is negative");
		
		}
		else
		{
			System.out.println(num+ "is neither positive nor negative");
		}
	}

	}
