package week1.day2;

public class Calculator {
	public int addNumber (int num1, int num2, int num3) {
		return (num1+num2+num3);	
    }

    public int subNumber (int num1, int num2) {
    	return (num1-num2);
    }
    public double mulNumber (double num1, double num2) {
    	return (num1*num2);
    }
    public float divnumber (float num1, float num2) {
    	return (num1/num2);
    }
}